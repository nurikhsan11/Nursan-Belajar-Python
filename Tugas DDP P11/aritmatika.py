angka1 = int(input('Masukkan angka yang anda inginkan =  '))
angka2 = int(input('Masukkan angka yang anda inginkan =  '))

def tambah ():
    hasil = angka1 + angka2
    print("Total pertambahan angka 1 + angka 2 adalah =   ",hasil)

def kurang ():
    hasil = angka1 - angka2
    print('Total pengurangan angka 1 - angka 2 adalah =   ',hasil)
    
def kali ():
    hasil = angka1  * angka2
    print('Total Perkalian angka 1 * angka 2 adalah =     ',hasil)