def Lingkaran(jari_jari):
    hasil = 22/7 * jari_jari * jari_jari
    print("hasil luas lingkaran adalah =  ",hasil)

def jajar_genjang(alas,tinggi):
    hasil = alas * tinggi
    print("Hasil luas jajar genjang adalah =  ",hasil)
    
def persegi(sisi):
    hasil = sisi * sisi
    print("Hasil luas persegi adalah =   ",hasil)
    
def persegi_panjang(p,l):
    hasil = p * l
    print("hasil luas persegi panjang adalah =   ",hasil)
    
def trapesium(a,c,t):
    hasil = 1/2 * (a+c) * t
    print('hasil luas Trapesium adalah =      ',hasil)

def segitiga(Alas,Tinggi):
    hasil = 1/2 * Alas * Tinggi
    print('hasil luas Segitiga adalah =       ',hasil)